package it.develhope.javadoc2;

/**
 * @author Giuseppe Iacono
 * class ArithmeticOperators that represent 4 methods
 */
public class ArithmeticOperators {
    /**
     * method that sum num1 and num2
     * @param num1 is the first int
     * @param num2 is the second int
     * @return int (num1+num2)
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }

    /**
     * method that makes the difference between num1 and num2
     * @param num1 is the first int
     * @param num2 is the second int
     * @return int (num1-num2)
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }

    /**
     * method that multiplies num1 and num2
     * @param num1 is the first int
     * @param num2 is the second int
     * @return int (num1*num2)
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }

    /**
     * method that divide num1 and num2
     * @param num1 is the first int
     * @param num2 is the second int
     * @return int (num1/num2)
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }
}
